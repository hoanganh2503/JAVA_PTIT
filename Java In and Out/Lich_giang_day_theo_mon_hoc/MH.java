public class MH {
    private String ma, ten, tin;

    public MH(String ma, String ten, String tin) {
        this.ma = ma;
        this.ten = ten;
        this.tin = tin;
    }

    public String getMa(){
        return this.ma;
    }

    public String getTen() {
        return this.ten;
    }
}
