package Examination.FOLDER.Viet_chuong_trinh_liet_ke_danh_sach_mon_hoc;

public class MH {
    private String ma, mon, ht;

    public MH(String ma, String mon, String ht) {
        this.ma = ma;
        this.mon = mon;
        this.ht = ht;
    }

    public String getMa() {
        return this.ma;
    }
    
    public String getMon() {
        return this.mon;
    }
    
    public String getHt() {
        return this.ht;
    }
}
