package Tinh_gia_cuoc_dien_thoai_co_dinh;

public class TT {
    private String ma, ten;
    private int gia;

    public TT(String ma, String ten, int gia) {
        this.ma = ma;
        this.ten = ten;
        this.gia = gia;
    }

    public String getMa() {
        return this.ma;
    }

    public String getTen() {
        return this.ten;
    }

    public int getGia() {
        return this.gia;
    }
}
